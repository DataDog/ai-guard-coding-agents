class _StubHandler(ProxyHandler):
    """Routing-test stub. ``matches()`` returns True iff the configured UA
    substring is present; ``parse_request`` / ``parse_response`` are no-ops so
    storage stays empty (these tests check routing, not persistence).
    """

    def __init__(
        self,
        agent: str,
        upstream: str,
        ua_substring: str,
        hook_reply: bytes = b"",
    ) -> None:
        self._agent = agent
        self._upstream = upstream
        self._ua_substring = ua_substring
        self._hook_reply = hook_reply
        self.hook_calls: list[tuple[str, bytes]] = []

    def agent(self) -> str:
        return self._agent

    def upstream(self) -> str:
        return self._upstream

    def matches(self, request: aiohttp.web.Request) -> bool:
        return self._ua_substring in request.headers.get("User-Agent", "")

    def parse_request(self, request: aiohttp.web.Request, body: bytes):
        return "", []

    def parse_response(self, response: aiohttp.ClientResponse, body: bytes):
        return []

    async def handle_hook(self, hook: str, payload: bytes) -> bytes:
        self.hook_calls.append((hook, payload))
        return self._hook_reply



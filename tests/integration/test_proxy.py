class TestProxyMultiHandler:
    """The per-agent contract with ≥2 handlers wired into the same proxy:

    * Each handler routes its claimed traffic to its **own** ``upstream()``
      — siblings' upstreams are never called.
    * When two handlers' ``matches()`` both return True, the **first** one
      in the handler list wins.
    * Hook traffic is dispatched by the **agent segment** in the URL
      (``/hook/<agent>/<hook>``), not by ``matches()`` and not by list
      order.
    """

    @staticmethod
    async def _marker_upstream(anthropic_server_factory, marker: str) -> str:
        """Boot a tiny upstream that echoes ``marker`` so the test can
        verify which upstream the proxy actually contacted."""

        async def handler(request: aiohttp.web.Request) -> aiohttp.web.Response:
            return aiohttp.web.json_response({"upstream": marker})

        return await anthropic_server_factory(handler)

    async def test_each_handler_routes_to_its_own_upstream(
        self,
        anthropic_server,
        multi_handler_proxy,
        storage_root: Path,
    ) -> None:
        url_alpha = await self._marker_upstream(anthropic_server, "alpha")
        url_beta = await self._marker_upstream(anthropic_server, "beta")
        client = await multi_handler_proxy(
            [
                _StubHandler(agent="alpha", upstream=url_alpha, ua_substring="alpha-cli"),
                _StubHandler(agent="beta", upstream=url_beta, ua_substring="beta-cli"),
            ]
        )

        async with client.post("/x", headers={"User-Agent": "alpha-cli/1.0"}) as resp:
            assert resp.status == 200
            assert (await resp.json()) == {"upstream": "alpha"}

        async with client.post("/x", headers={"User-Agent": "beta-cli/1.0"}) as resp:
            assert resp.status == 200
            assert (await resp.json()) == {"upstream": "beta"}

    async def test_first_matching_handler_wins(
        self,
        anthropic_server,
        multi_handler_proxy,
        storage_root: Path,
    ) -> None:
        url_first = await self._marker_upstream(anthropic_server, "first")
        url_second = await self._marker_upstream(anthropic_server, "second")
        # Both handlers claim the same UA — list order decides.
        client = await multi_handler_proxy(
            [
                _StubHandler(agent="first", upstream=url_first, ua_substring="cli/"),
                _StubHandler(agent="second", upstream=url_second, ua_substring="cli/"),
            ]
        )

        async with client.post("/x", headers={"User-Agent": "any-cli/1.0"}) as resp:
            assert resp.status == 200
            assert (await resp.json()) == {"upstream": "first"}

    async def test_hook_dispatches_by_agent_segment_not_list_order(
        self,
        multi_handler_proxy,
        storage_root: Path,
    ) -> None:
        # Both handlers' matches() return False (sentinel UA never appears),
        # so proxy traffic would 502; this test only exercises the hook path.
        h_alpha = _StubHandler(
            agent="alpha",
            upstream="http://invalid.example",
            ua_substring="never",
            hook_reply=b'{"r": "alpha"}',
        )
        h_beta = _StubHandler(
            agent="beta",
            upstream="http://invalid.example",
            ua_substring="never",
            hook_reply=b'{"r": "beta"}',
        )
        client = await multi_handler_proxy([h_alpha, h_beta])

        async with client.post(
            "/hook/beta/session-start",
            data=b'{"x": 1}',
            headers={"User-Agent": HOOK_UA},
        ) as resp:
            assert resp.status == 200
            assert (await resp.json()) == {"r": "beta"}

        assert h_alpha.hook_calls == []
        assert h_beta.hook_calls == [("session-start", b'{"x": 1}')]



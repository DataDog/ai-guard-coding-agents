class _RecordingHandler(ProxyHandler):
    """Minimal handler that records hook invocations and returns canned bytes."""

    def __init__(self, agent_name: str = "claude", reply: bytes = b"") -> None:
        self._agent = agent_name
        self._reply = reply
        self.calls: list[tuple[str, bytes]] = []
        self.fail = False

    def agent(self) -> str:
        return self._agent

    def upstream(self) -> str:
        return "http://upstream.invalid"

    def matches(self, request: aiohttp.web.Request) -> bool:
        return False

    def parse_request(self, request: aiohttp.web.Request, body: bytes):
        return "", []

    def parse_response(self, response: aiohttp.ClientResponse, body: bytes):
        return []

    async def handle_hook(self, hook: str, payload: bytes) -> bytes:
        self.calls.append((hook, payload))
        if self.fail:
            raise RuntimeError("boom")
        return self._reply



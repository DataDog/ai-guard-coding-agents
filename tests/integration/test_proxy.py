"""Integration tests for the HTTP proxy.

The proxy is per-agent: every handler declares its own ``upstream()`` and
claims requests via ``matches()`` (User-Agent only). There is no default
upstream — a request no handler claims gets 502.

Grouped by what's being exercised:

  * :class:`TestProxyPassthrough` — Claude requests claimed by ``ClaudeProxy``
    are forwarded to its upstream and persisted to storage. Includes the
    "matches() is UA-only" edge case (off-path Claude UA still matches).
  * :class:`TestProxyStreaming` — SSE responses pass through byte-for-byte
    while the assistant message is reconstructed and stored.
  * :class:`TestProxyUnclaimed` — no handler claims the request → 502.
  * :class:`TestProxyMultiHandler` — multiple handlers, each with its own
    upstream; first matching handler wins; hooks dispatch by agent segment.
  * :class:`TestProxyHookRouting` — ``/hook/<agent>/<hook>`` URL dispatch
    (User-Agent contains ``ai-guard-cli``).
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

import aiohttp
import aiohttp.web
import pytest
from aiohttp.test_utils import TestServer

from aiguard import storage
from aiguard.proxy.server import Proxy, ProxyHandler

CLAUDE_UA = "claude-cli/1.2.3 (External, cli)"
HOOK_UA = "ai-guard-cli/test"
FIXTURE_SSE = Path(__file__).resolve().parents[1] / "fixtures" / "anthropic_sse_stream.txt"


# ── Passthrough + storage ─────────────────────────────────────────────────────


def _anthropic_handler(response_body: dict[str, Any]) -> Any:
    async def handler(request: aiohttp.web.Request) -> aiohttp.web.Response:
        return aiohttp.web.json_response(response_body)

    return handler


class TestProxyPassthrough:
    async def test_returns_upstream_body_and_persists_messages(
        self,
        proxy_client,
        storage_root: Path,
        anthropic_request_body: dict[str, Any],
        anthropic_response_body: dict[str, Any],
    ) -> None:
        client = await proxy_client(_anthropic_handler(anthropic_response_body))

        async with client.post(
            "/v1/messages",
            json=anthropic_request_body,
            headers={"User-Agent": CLAUDE_UA, "x-api-key": "sk-test"},
        ) as resp:
            assert resp.status == 200
            body = await resp.json()
            assert body["id"] == anthropic_response_body["id"]

        # Session id is embedded in metadata.user_id JSON in the request body.
        stored = storage.load_messages("claude", "sess-abc")
        roles = [m["role"] for m in stored]
        assert roles[0] == "system"
        assert "assistant" in roles
        assert stored[-1]["role"] == "assistant"

    async def test_strips_hop_by_hop_request_headers(
        self,
        proxy_client,
        anthropic_request_body: dict[str, Any],
        anthropic_response_body: dict[str, Any],
        storage_root: Path,
    ) -> None:
        """Headers in ``_SKIP_REQ_HEADERS`` (host, content-length,
        transfer-encoding) must not be forwarded verbatim — aiohttp regenerates
        them for the upstream connection. Auth headers like ``x-api-key`` must
        survive the hop.
        """
        seen_headers: dict[str, str] = {}
        proxy_host_header = "the-proxys-public-name.invalid"

        async def handler(request: aiohttp.web.Request) -> aiohttp.web.Response:
            seen_headers.update(dict(request.headers))
            return aiohttp.web.json_response(anthropic_response_body)

        client = await proxy_client(handler)
        body = json.dumps(anthropic_request_body).encode()
        async with client.post(
            "/v1/messages",
            data=body,
            headers={
                "User-Agent": CLAUDE_UA,
                "Content-Type": "application/json",
                "x-api-key": "kept",
                # A bogus Host the client thinks it's reaching — must NOT
                # survive the hop; aiohttp regenerates Host for the upstream.
                "Host": proxy_host_header,
            },
        ) as resp:
            assert resp.status == 200

        lower = {k.lower(): v for k, v in seen_headers.items()}

        # Auth header survives.
        assert lower.get("x-api-key") == "kept"

        # Host is regenerated by aiohttp to point at the upstream — the
        # client-provided Host MUST NOT leak to the upstream.
        assert lower.get("host") != proxy_host_header

        # Content-Length is regenerated by aiohttp from the actual body. The
        # client-supplied bytes are intact upstream (length matches body).
        assert lower.get("content-length") == str(len(body))

    async def test_request_replaces_then_response_appends(
        self,
        proxy_client,
        storage_root: Path,
        anthropic_request_body: dict[str, Any],
        anthropic_response_body: dict[str, Any],
    ) -> None:
        """Two realistic turns: every request carries the full history (Anthropic
        Messages API is stateless), the proxy replaces on request and appends on
        response — no duplication."""

        async def handler(request: aiohttp.web.Request) -> aiohttp.web.Response:
            return aiohttp.web.json_response(anthropic_response_body)

        client = await proxy_client(handler)

        # Turn 1
        async with client.post(
            "/v1/messages",
            json=anthropic_request_body,
            headers={"User-Agent": CLAUDE_UA},
        ):
            pass

        after_turn1 = storage.load_messages("claude", "sess-abc")
        assert after_turn1
        assert after_turn1[-1]["role"] == "assistant"

        # Turn 2: client adds the assistant reply from turn 1 plus a new user prompt.
        turn2_body = {
            **anthropic_request_body,
            "messages": [
                *anthropic_request_body["messages"],
                {
                    "role": "assistant",
                    "content": [{"type": "text", "text": "All done."}],
                },
                {"role": "user", "content": "Anything else?"},
            ],
        }
        async with client.post(
            "/v1/messages",
            json=turn2_body,
            headers={"User-Agent": CLAUDE_UA},
        ):
            pass

        after_turn2 = storage.load_messages("claude", "sess-abc")
        # Expected: prior history (replaced from turn-2 request body) + new response.
        assert len(after_turn2) == len(after_turn1) + 2
        assert after_turn2[-1]["role"] == "assistant"
        # The "Anything else?" prompt from turn 2's request is present exactly once.
        user_texts = [m for m in after_turn2 if m.get("role") == "user"]
        assert sum(1 for m in user_texts if "Anything else?" in str(m.get("content", ""))) == 1

    async def test_claude_ua_with_off_path_still_forwards_upstream(
        self, proxy_client, anthropic_response_body, storage_root: Path
    ) -> None:
        """``ClaudeProxy.matches()`` is User-Agent only — method, path, and
        content-type are ``parse_request``'s concern. So a Claude UA with an
        off-spec path still gets claimed and forwarded; ``parse_request`` then
        returns no messages, so nothing is persisted."""

        async def handler(request: aiohttp.web.Request) -> aiohttp.web.Response:
            return aiohttp.web.json_response(anthropic_response_body)

        client = await proxy_client(handler)
        async with client.get("/v1/anything", headers={"User-Agent": CLAUDE_UA}) as resp:
            assert resp.status == 200

        assert not storage_root.exists() or not any(storage_root.rglob("*.json"))


# ── Streaming ────────────────────────────────────────────────────────────────


class TestProxyStreaming:
    async def test_streaming_passthrough_persists_assistant_message(
        self,
        proxy_client,
        storage_root: Path,
        anthropic_request_body: dict[str, Any],
    ) -> None:
        sse_body = FIXTURE_SSE.read_bytes()

        async def handler(request: aiohttp.web.Request) -> aiohttp.web.StreamResponse:
            resp = aiohttp.web.StreamResponse(
                status=200, headers={"Content-Type": "text/event-stream"}
            )
            await resp.prepare(request)
            mid = len(sse_body) // 2
            await resp.write(sse_body[:mid])
            await resp.write(sse_body[mid:])
            await resp.write_eof()
            return resp

        streamed = {**anthropic_request_body, "stream": True}
        client = await proxy_client(handler)
        async with client.post(
            "/v1/messages",
            json=streamed,
            headers={"User-Agent": CLAUDE_UA},
        ) as resp:
            assert resp.status == 200
            assert await resp.read() == sse_body

        stored = storage.load_messages("claude", "sess-abc")
        assistants = [m for m in stored if m.get("role") == "assistant"]
        assert assistants, "expected reconstructed assistant message in storage"
        last = assistants[-1]
        text_part = next((p for p in last.get("content", []) if p["type"] == "text"), None)
        assert text_part is not None
        assert text_part["text"] == "Hello world"
        tool_call = last.get("tool_calls", [])[0]
        assert tool_call["function"]["name"] == "Read"
        assert json.loads(tool_call["function"]["arguments"]) == {"path": "README.md"}


# ── Unclaimed traffic ─────────────────────────────────────────────────────────


class TestProxyUnclaimed:
    """The proxy is per-agent: each handler declares its own ``upstream()`` and
    claims requests via ``matches()``. There is no default upstream — when no
    registered handler claims a request, the proxy returns **502**. The body
    explains why, and no upstream is contacted."""

    @staticmethod
    def _unreachable_upstream(_: object):
        async def handler(request: aiohttp.web.Request) -> aiohttp.web.Response:
            raise AssertionError("upstream must not be hit when no handler claims the request")

        return handler

    @staticmethod
    def _assert_no_storage(storage_root: Path) -> None:
        assert not storage_root.exists() or not any(storage_root.rglob("*.json"))

    async def test_unknown_ua_returns_502(self, proxy_client, storage_root: Path) -> None:
        client = await proxy_client(self._unreachable_upstream(None))

        async with client.get("/v1/health", headers={"User-Agent": "curl/8.0"}) as resp:
            assert resp.status == 502
            # Lock in the body contract: the proxy explains why it dropped the
            # request, citing method + path. A future change that swaps the
            # status without an explanation should fail this assertion.
            assert "no handler claims" in (await resp.text())

        self._assert_no_storage(storage_root)

    async def test_no_ua_returns_502(self, proxy_client, storage_root: Path) -> None:
        client = await proxy_client(self._unreachable_upstream(None))

        async with client.post("/v1/messages", data=b"raw") as resp:
            assert resp.status == 502
            assert "no handler claims" in (await resp.text())

        self._assert_no_storage(storage_root)


